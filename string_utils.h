#ifndef STRING_UTILS_H
#define STRING_UTILS_H

/*
    קורא מילה אחת מתוך שורת קלט.
    עוצר בקריאה על רווח, פסיק או סוף שורה.
    מקבל מצביע למצביע לשורת הקלט, ומחזיר מחרוזת חדשה שצריך לשחרר.
*/
char* read_word(char** line);

/*
    מסיר רווחים מההתחלה ומהסוף של מחרוזת.
    מחזיר מצביע לתחילת הטקסט הנקי.
*/
char* trim_whitespace(char* str);

/*
    אם מחרוזת מוקפת בגרשיים ("), מסיר אותם ומחזיר מצביע למחרוזת הפנימית.
    אחרת מחזיר את המחרוזת המקורית.
*/
char* trim_quotes(char* str);

/*
    יוצר עותק חדש של מחרוזת עם הקצאת זיכרון דינמית.
*/
char* duplicateString(const char* src);

#endif
